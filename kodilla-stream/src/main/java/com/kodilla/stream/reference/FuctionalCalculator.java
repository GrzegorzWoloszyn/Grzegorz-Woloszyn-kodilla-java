package com.kodilla.stream.reference;

public class FuctionalCalculator {
    public static double multiplayAByB(double a, double b) {
        return a * b;
    }

    public static double addAToB(double a, double b) {
        return a + b;
    }

    public static double subAFromB(double a, double b) {
        return a - b;
    }

    public static double divideAByB(double a, double b) {
        return a / b;
    }
}
